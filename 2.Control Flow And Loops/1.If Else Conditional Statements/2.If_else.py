# -----------------------------------------------
# If-Else Statement in Python
# -----------------------------------------------
# This program checks if the user is eligible to drive based on age
# -----------------------------------------------

# Ask the user to input their age
age = int(input("Enter your age: "))

# Check if age is greater than 18
if age > 18:
    print("You can drive.")       # Executed if the condition is True
else:
    print("You cannot drive.")    # Executed if the condition is False
