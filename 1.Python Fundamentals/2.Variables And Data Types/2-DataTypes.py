# -----------------------------------------------
# Basic Data Types in Python (Introduction Level)
# -----------------------------------------------

# Integer → Whole numbers (positive or negative, no decimals)
age = 5
print("Type of age:", type(age))  # <class 'int'>

# Float → Numbers with decimal points
height_in_feet = 7.2
print("Type of height_in_feet:", type(height_in_feet))  # <class 'float'>

# String → Text enclosed in quotes
user_name = "Aryaman"
print("Type of user_name:", type(user_name))  # <class 'str'>

# Boolean → Represents True or False values
is_active = True
print("Type of is_active:", type(is_active))  # <class 'bool'>

# --------------------------------------------------
# Summary of Common Data Types:
# Integer  → Whole Numbers       → e.g., 10, -3, 0
# Float    → Decimal Numbers     → e.g., 5.6, -2.1
# String   → Text                → e.g., "Hello", 'Python'
# Boolean  → Logical Values      → e.g., True, False
