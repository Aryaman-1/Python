# -----------------------------------------------
# Escape Sequences in Python Strings
# -----------------------------------------------

# \n → Newline (moves to the next line)
print("Hello, how are you?\nI am good.")

# \t → Tab space (adds horizontal space)
print("Hello, this is a tab\tspace.")

# \\ → Prints a single backslash
print("This is a backslash: \\")

# \" → Prints double quotes inside a string
print("\"This is in a double quote\"")

# \' → Prints single quotes inside a string
print("\'This is in a single quote\'")