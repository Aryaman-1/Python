# -----------------------------------------------
# Arithmetic Operators in Python
# -----------------------------------------------

num1 = 25
num2 = 5

# Perform basic arithmetic operations
print("Addition (num1 + num2):", num1 + num2)         # Adds two numbers → 25 + 5 = 30
print("Subtraction (num1 - num2):", num1 - num2)      # Subtracts second from first → 25 - 5 = 20
print("Multiplication (num1 * num2):", num1 * num2)   # Multiplies two numbers → 25 * 5 = 125
print("Division (num1 / num2):", num1 / num2)         # Divides first by second → 25 / 5 = 5.0

# Additional arithmetic operations
print("Modulus (num1 % num2):", num1 % num2)          # Remainder of division → 25 % 5 = 0
print("Floor Division (num1 // num2):", num1 // num2) # Division result without decimals → 25 // 5 = 5
print("Exponentiation (num1 ** num2):", num1 ** num2) # Raises num1 to the power of num2 → 25^5
