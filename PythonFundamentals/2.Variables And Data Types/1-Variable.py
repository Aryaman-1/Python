# Variable Declarations with Data Types and Naming Rules

# An integer representing a person's age
person_age = 34  # Integer

# A floating-point number representing a product price
product_price = 23.4  # Floating point

# A string representing a person's name
person_name = "Aryaman"  # String

# A boolean indicating if the user is logged in
is_logged_in = True  # Boolean (True/False)

