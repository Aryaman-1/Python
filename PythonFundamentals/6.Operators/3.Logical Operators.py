# -----------------------------------------------
# Logical Operators in Python
# -----------------------------------------------
# These operators are used to perform logical operations on boolean values (True or False)
# -----------------------------------------------

# AND Operator → Returns True only if **both** values are True
print("For 'and' Operator:")
print("True and True   →", True and True)    # True
print("True and False  →", True and False)   # False
print("False and True  →", False and True)   # False
print("False and False →", False and False)  # False

# OR Operator → Returns True if **at least one** value is True
print("\nFor 'or' Operator:")
print("True or True    →", True or True)     # True
print("True or False   →", True or False)    # True
print("False or True   →", False or True)    # True
print("False or False  →", False or False)   # False

# NOT Operator → Reverses the boolean value
print("\nFor 'not' Operator:")
print("not True        →", not True)         # False
print("not False       →", not False)        # True
